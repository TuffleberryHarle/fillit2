#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		n += 2000000000;
		n *= -1;
	}
	if (n < 0)
	{
		n *= -1;
		ft_putchar(45);
	}
	if (n < 10)
	{
		ft_putchar(n + 48);
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
